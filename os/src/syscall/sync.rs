/// semaphore create syscall
pub fn sys_semaphore_create(res_count: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_create",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let id = if let Some(id) = process_inner
        .semaphore_list
        .iter()
        .enumerate()
        .find(|(_, item)| item.is_none())
        .map(|(id, _)| id)
    {
        process_inner.semaphore_list[id] = Some(Arc::new(Semaphore::new(res_count)));
        id
    } else {
        process_inner
            .semaphore_list
            .push(Some(Arc::new(Semaphore::new(res_count))));
        //为semaphore创建need与alloc向量
        if process_inner.semaphore_need.is_empty() {
            process_inner.semaphore_need.push(Vec::new());
            process_inner.semaphore_alloc.push(Vec::new());
        }
        process_inner.semaphore_alloc[0].push(0);
        process_inner.semaphore_need[0].push(0);
        process_inner.semaphore_list.len() - 1
    };
    id as isize
}
/// semaphore up syscall
pub fn sys_semaphore_up(sem_id: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_up",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut  process_inner = process.inner_exclusive_access();
    if process_inner.enable_detect {
        let tid = current_task().unwrap().inner_exclusive_access().res.as_ref().unwrap().tid;
        process_inner.semaphore_alloc[tid][sem_id] -= 1;
    }
    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());
    drop(process_inner);
    sem.up();
    0
}
/// semaphore down syscall
pub fn sys_semaphore_down(sem_id: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_down",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());
    if process_inner.enable_detect {
        
        let tid = current_task().unwrap().inner_exclusive_access().res.as_ref().unwrap().tid;
        process_inner.semaphore_need[tid][sem_id] += 1;
        //准备数据结构
        let task_num = process_inner.thread_count();
        let mut finish_list: Vec<bool> = Vec::new();
        for _ in 0..task_num {
            finish_list.push(false);
        }
        let mut work = process_inner.generate_work_vec();
        let mut alloc = process_inner.semaphore_alloc.clone();
        let mut need = process_inner.semaphore_need.clone();
        let mut finish_count = 0;
        //假定分配
        if work[sem_id] > 0 {
            alloc[tid][sem_id] += 1;
            need[tid][sem_id] -= 1;
            work[sem_id] -= 1;
        } else {
            return -0xDEAD;
        }
        //安全性检查
        let thread_num = alloc.len();
        let sem_num = alloc[0].len();
        for _ in 0..thread_num {
            for _tid in 0..thread_num {
                if finish_list[_tid] {
                    continue;
                } else {
                    //对比work与need
                    let mut less_or_eq = true;
                    for j in 0..sem_num {
                        if need[_tid][j] > work[j] as i32{
                            less_or_eq = false;
                        }
                    }
                    if less_or_eq {
                        finish_list[_tid] = true;
                        finish_count += 1;
                        //work += alloc
                        for j in 0..sem_num {
                            work[j] += alloc[_tid][j] as usize;
                        }
                        break;
                    }
                }
            }
        }
        //若有未完成线程，说明不安全
        if finish_count != thread_num {
            return -0xDEAD;
        }
        //安全，分配
        process_inner.semaphore_alloc[tid][sem_id] += 1;
        process_inner.semaphore_need[tid][sem_id] -= 1;
    }
    drop(process_inner);
    sem.down();
    0
}

